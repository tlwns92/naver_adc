			<div class="post">
				<h2 class="title">What is the ADC Study?</h2>
				<div class="entry">
					<ul style="font-size: 15.008px;"><li>An international comparative effectiveness study to determine the best way to manage chronic coronary disease in patients with advanced chronic kidney disease (eGFR &lt;30 or on dialysis).</li>
						<li>Patients with advanced CKD with moderate to severe ischemia on stress testing were randomized to this Study.  </li></ul>
				</div>
			</div>
			
			<div class="post">
				<h2 class="title">What is ADC? </h2>
				<div class="entry">
					<ul style="font-size: 15
					.008px;"><li>ISCHEMIA-EXTEND is the long-term follow-up of randomized, surviving participants in the ISCHEMIA and ISCHEMIA-CKD trials.</li>
						<li>ISCHEMIA-EXTEND assesses whether an initial invasive strategy—cardiac catheterization and revascularization when feasible plus optimal medical therapy (OMT)—reduces long-term all-cause mortality as compared with an initial 
						conservative strategy of OMT for chronic coronary disease patients with moderate or severe ischemia over an extended 5 year period of follow-up.</li></ul>
				</div>
			</div>
			
			
			
			
			
			
			
			<div class="post">
				<h2 class="title">Why is the ADC Study Important?</h2>
				<div class="entry">
					<ul style="font-size: 15.008px;"><li>Chronic coronary disease is the leading cause of death and disability worldwide and affects an estimated 18,200,000 Americans, resulting in about 541,000 deaths in the United States annually. Globally, 8.9 million deaths are caused by IHD each year. Medical therapy (medication and lifestyle changes) 
					should always be used to treat IHD. The trial showed that heart procedures added to taking medicines and making lifestyle changes did not reduce the overall rate of cardiovascular death and heart attack compared with medicines and lifestyle changes alone. However, for people with chest pain symptoms, heart procedures improved symptoms 
					better than medicines and lifestyle changes alone. The more chest pain to begin with, the more symptoms improved after getting a stent or bypass surgery.</li>
						<li>For ISCHEMIA Study Results, please click <a href="/ischemia-study-results-presented-aha-2019">here</a>.</li></ul>
				</div>
			</div>
			
			
			<div class="post">
				<h2 class="title">Why is ADC Important?</h2>
				<div class="entry">
					<ul style="font-size: 15.008px;"><li>The ADC and ISCHEMIA-CKD trials did not demonstrate a reduction in their primary endpoints with an initial invasive strategy. All-cause mortality was similar over 5 years. There was an early excess of peri-procedural MI and a late reduction in spontaneous MI in both trials. 
					Prior evidence demonstrates that spontaneous MI has a larger impact on subsequent death than most peri-procedural MI’s. Therefore, it is imperative to assess long-term all-cause mortality to provide patients and clinicians with robust evidence regarding survival following the two initial management strategies 
					over the long-term (~10 years).</li>
					<li>Understanding the impact of nonfatal events on subsequent mortality among patients with chronic coronary disease will influence clinical practice and the design of cardiovascular clinical trials for years to come. With the ever-increasing sensitivity of biomarker assays for MI, it is of paramount importance to understand 
					the relationship between MI and subsequent death.</li></ul>
				</div>
			</div>
			
			
			
			
			

			
